// Build new-style ending: returns { boyClose, girlLines } where girlLines is an array
function pickUniqueFromPool(pool, maxChars, usedSet) {
  const candidates = Array.isArray(pool) ? pool : [];
  const available =
    usedSet && usedSet.size
      ? candidates.filter((line) => !usedSet.has(String(line || "").toLowerCase()))
      : candidates;
  const selected = pickFallbackLine(available.length ? available : candidates, maxChars);
  if (usedSet && selected) usedSet.add(String(selected).toLowerCase());
  return selected;
}

function buildNewEnding(rng, spice, maxChars, usedGirlLines, options = {}) {
  const number = buildFakePhoneNumber(rng);
  const day = pick(rng, WIN_DAY_OPTIONS);
  const time = pick(rng, WIN_TIME_OPTIONS);
  const forcedType = options && (options.forceType === "B" || options.forceType === "C")
    ? options.forceType
    : null;
  const useTypeC = forcedType ? forcedType === "C" : (spice === "high" ? rng() < 0.4 : rng() < 0.15);

  if (useTypeC) {
    // Type C: boy asks → girl resists → boy holds frame → girl gives number → girl teases
    const boyAsk = pick(rng, BOY_NUMBER_ASK_LINES);
    const girlResist = pickUniqueFromPool(GIRL_RESIST_LINES, maxChars, usedGirlLines);
    const boyFrame = pick(rng, BOY_FRAME_LINES);
    const leadInTemplate = pick(rng, GIRL_NUMBER_LEAD_INS);
    const leadIn = leadInTemplate.replace("{number}", number);
    const tease = pickUniqueFromPool(GIRL_TEASE_LINES, maxChars, usedGirlLines);
    return {
      type: "C",
      endingMessages: [
        { from: "boy", text: boyAsk },
        { from: "girl", text: girlResist },
        { from: "boy", text: boyFrame },
        { from: "girl", text: leadIn },
        { from: "girl", text: tease }
      ]
    };
  }

  // Type B: boy proposes plan → girl drops number → girl teases
  const boyTemplate = pick(rng, BOY_CLOSE_TEMPLATES);
  const boyClose = boyTemplate.replace("{day}", day).replace("{time}", time);
  const leadInTemplate = pick(rng, GIRL_NUMBER_LEAD_INS);
  const leadIn = leadInTemplate.replace("{number}", number);
  const tease = pickUniqueFromPool(GIRL_TEASE_LINES, maxChars, usedGirlLines);
  return {
    type: "B",
    endingMessages: [
      { from: "boy", text: boyClose },
      { from: "girl", text: leadIn },
      { from: "girl", text: tease }
    ]
  };
}

function extractDay(text) {
  if (!text) return "";
  const lower = text.toLowerCase();
  for (const day of WIN_DAY_OPTIONS) {
    const pattern = new RegExp(`\\b${day}\\b`, "i");
    if (pattern.test(lower)) return day;
  }
  return "";
}

