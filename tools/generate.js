  if (/\b(and\?|so what|cap|nah|no|why|huh|wdym|wym|prove it|really|says who|who said)\b/.test(cleaned)) return true;
  if (/\b(try harder|convince me|youre bold|you talk big|earned it|if )\b/.test(cleaned)) return true;
  if (/\?$/.test(String(text || "").trim())) return true;
