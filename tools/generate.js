function parseHookLines(raw) {
  return raw
    .split(/\n\s*\n/)
    .map((block) =>
      block
        .split(/\n/)
        .map((line) => line.trim())
        .filter(Boolean)
        // Skip markdown headers and comments (lines starting with #)
        .filter((line) => !line.startsWith("#"))
    )
    .filter((lines) => lines.length > 0)
    .map((lines) => {
      const headlineLines = [];
      const subtitleLines = [];
      lines.forEach((line, index) => {
        if (index === 0) {
          headlineLines.push(line);
          return;
        }
        if (/^[*(]/.test(line) || subtitleLines.length > 0) {
          subtitleLines.push(line);
          return;
        }
        headlineLines.push(line);
      });
      return {
        headline: headlineLines.join(" ").trim(),
        subtitle: subtitleLines.join(" ").trim()
      };
    })
    .filter((entry) => entry.headline);
}

