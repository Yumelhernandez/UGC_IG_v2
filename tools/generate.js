function scoreReply(text) {
  const cleaned = normalizeMessageText(text);
  if (!cleaned) return -Infinity;
  let score = 0;
  const wordCount = cleaned.split(/\s+/).filter(Boolean).length;
  if (/\?/.test(cleaned)) score += 2;
  if (HOOKY_REPLY_PATTERNS.some((pattern) => pattern.test(cleaned))) score += 2;
  if (CONTROVERSIAL_REPLY_PATTERNS.some((pattern) => pattern.test(cleaned))) score += 4;
  if (WEAK_OPENER_PATTERNS.some((pattern) => pattern.test(cleaned))) score -= 3;
  if (wordCount >= 6 && wordCount <= 16) score += 2;
  if (wordCount >= 4 && wordCount <= 20) score += 1;
  return score;
}

