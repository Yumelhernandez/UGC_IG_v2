  const firstThree = messages
    .slice(0, 3)
    .map((m) => normalizeMessageText((m && m.text) || "").toLowerCase())
    .join(" ");
  const token =
    captionTokens.find((t) => !["this", "that", "with", "from", "your", "just", "have"].includes(t)) ||
    captionTokens[0];
