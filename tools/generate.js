function pickUniqueLineWithRng(pool, usedSet, maxChars, rng) {
  const available = usedSet
    ? pool.filter((line) => !usedSet.has(line.toLowerCase()))
    : pool;
  const source = available.length ? available : pool;
  const sized = source.filter((line) => line.length <= maxChars);
  const candidatePool = sized.length ? sized : source;
  const index = rng ? Math.floor(rng() * candidatePool.length) : Math.floor(Math.random() * candidatePool.length);
  const choice = candidatePool[Math.max(0, Math.min(candidatePool.length - 1, index))];
  if (usedSet && choice) usedSet.add(choice.toLowerCase());
  return choice;
}

function looksNonEnglish(text) {
  if (!text) return false;
  // Strip emojis and common Unicode punctuation before checking for non-ASCII
  const stripped = text.replace(/[\u{1F000}-\u{1FFFF}]|[\u{2600}-\u{27BF}]|[\u{FE00}-\u{FEFF}]|[\u{200D}\u{20E3}\u{FE0F}\u{FE0E}]|[\u{E0020}-\u{E007F}]|[\u{1F900}-\u{1F9FF}]|[\u{1FA00}-\u{1FA6F}]|[\u{1FA70}-\u{1FAFF}]|[\u{2702}-\u{27B0}]|[\u{231A}-\u{23F3}]|[\u{2300}-\u{23FF}]|[\u{2934}-\u{2935}]|[\u{25AA}-\u{25FE}]|[\u{2B05}-\u{2B07}]|[\u{2B1B}-\u{2B1C}]|[\u{3030}\u{303D}]|[\u{3297}\u{3299}]|[\u{200B}-\u{200F}]|[\u2018\u2019\u201C\u201D\u2014\u2013\u2026]/gu, "");
  if (/[^\x00-\x7F]/.test(stripped)) return true;
  const lower = text.toLowerCase();
  let hits = 0;
  for (const word of SPANISH_WORDS) {
    const pattern = new RegExp(`\\b${word}\\b`, "i");
    if (pattern.test(lower)) hits += 1;
    if (hits >= 2) return true;
  }
  return false;
}

