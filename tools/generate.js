function collapseToSingleSentence(text) {
  const trimmed = text.trim().replace(/\s+/g, " ");
  if (!trimmed) return trimmed;
  const hasQuestion = trimmed.includes("?");
  const hasExclamation = trimmed.includes("!");
  const merged = trimmed
    .replace(/[.!?]\s*/g, ", ")
    .replace(/\s*,\s*/g, ", ")
    .replace(/,\s*$/, "");
  if (hasQuestion) return `${merged}?`;
  if (hasExclamation) return `${merged}!`;
  return merged;
}

function normalizeMessageText(text) {
  if (!text) return "";
  let cleaned = collapseToSingleSentence(text);
  cleaned = cleaned.replace(/-/g, " ");
  cleaned = cleaned
    .replace(/\bI am\b/gi, "im")
    .replace(/\bI have\b/gi, "ive")
    .replace(/\bI will\b/gi, "ill")
    .replace(/\bI would\b/gi, "id")
    .replace(/\bdo not\b/gi, "dont")
    .replace(/\bcan not\b/gi, "cant")
    .replace(/\bcannot\b/gi, "cant")
    .replace(/\byou are\b/gi, "youre")
    .replace(/\bwe are\b/gi, "were")
    .replace(/\bthey are\b/gi, "theyre");
  cleaned = cleaned.replace(/\s+/g, " ").trim();
  cleaned = cleaned.replace(/,\s*,+/g, ", ");
  cleaned = cleaned.replace(/(?:^|\s),\s*,+/g, ", ");
  cleaned = cleaned.replace(/^(?:[,:;]+\s*)+/, "").trim();
  return cleaned;
}

function cleanCasualLine(text) {
  if (!text) return text;
  let cleaned = text.trim().replace(/\s+/g, " ");
  cleaned = cleaned.replace(/\s+([?!.,])/g, "$1");
  cleaned = cleaned.replace(/,\s*\?/g, "?");
  return cleaned;
}

function sentenceCaseGirlLine(text) {
  if (!text) return text;
  const trimmed = cleanCasualLine(text);
  if (!trimmed) return text;
  const firstChar = trimmed.charAt(0);
  if (/[a-z]/.test(firstChar)) {
    return firstChar.toUpperCase() + trimmed.slice(1);
  }
  return trimmed;
}

function clampMessageText(text, maxChars) {
  if (!text) return "";
  if (!Number.isFinite(maxChars) || maxChars <= 0) return text;
  if (text.length <= maxChars) return text;
  const sliced = text.slice(0, maxChars).trim();
  let trimmed = sliced.replace(/\s+\S*$/, "").trim();
  // After removing the last word, keep stripping if we're left with a dangling
  // connector (preposition, article, conjunction, linking verb) — e.g. "…is a public"
  // becomes "…" rather than stopping at "…is a".
  const connectorRe = /\s+(a|an|the|is|are|was|were|be|been|to|in|on|at|for|of|and|but|or|so|nor|yet|with|from|by|as|that|this|it|i|you|he|she|we|they|your|my|its|their|our|not|no|just|when|if|then|also|even|like|still|though|than|because|since|about|into|out|up|down|over|after|before|now|am|will|can|do|did|have|has|had|get|got|goes|how|who|what|why|where|which|there|here|some|any|all|more|very|too|so|quite|really|maybe|just|only|never|always|already|again|once|twice)\s*$/i;
  let safetyCount = 0;
  while (connectorRe.test(trimmed) && safetyCount < 6) {
    trimmed = trimmed.replace(/\s+\S+\s*$/, "").trim();
    safetyCount++;
  }
  return trimmed || sliced;
}

function containsName(text, name) {
  if (!text || !name) return false;
  const escaped = name.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
  const pattern = new RegExp(`\\b${escaped}\\b`, "i");
  return pattern.test(text);
}

function hasAnyPhrase(text, phrases) {
  return phrases.some((phrase) => {
    if (!phrase) return false;
    const escaped = phrase.trim().replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
    const spaced = escaped.replace(/\s+/g, "\\s+");
    const pattern = new RegExp(`\\b${spaced}\\b`, "i");
    return pattern.test(text);
  });
}

