function bankPool(bankKey, hardcodedFallback) {
  const bankLines = Array.isArray(_lineBanks[bankKey]) ? _lineBanks[bankKey] : [];
  if (bankLines.length === 0) return hardcodedFallback;
  const bankSet = new Set(bankLines.map((l) => l.toLowerCase()));
  const extra = hardcodedFallback.filter((l) => !bankSet.has(l.toLowerCase()));
  return [...bankLines, ...extra];
}

