function buildArcPlan({ count, config, seed }) {
  if (!Number.isFinite(count) || count <= 0) return [];
  const rng = createRng(seed);
  const distribution = config.arc_distribution || {
    number_exchange: 0.6,
    rejection: 0.2,
    plot_twist: 0.05,
    cliffhanger: 0.15
  };
  const comedyWeight = Number(distribution.comedy || 0);
  if (count === 20 && comedyWeight <= 0) {
    const fixed = { number_exchange: 12, rejection: 4, plot_twist: 1, cliffhanger: 3 };
    const plan = [];
    Object.entries(fixed).forEach(([arc, qty]) => {
      for (let i = 0; i < qty; i += 1) plan.push(arc);
    });
    return shuffleWithRng(plan, rng);
  }
  const plan = buildTierPlan({ count, distribution, seed });
  const supportedArcs = ["number_exchange", "rejection", "plot_twist", "cliffhanger", "comedy", "brainrot"];
  // Only require arcs that are explicitly enabled (weight > 0) in the distribution.
  // Arcs set to 0 in config are intentionally excluded — do not force them back in.
  const requiredArcs = supportedArcs.filter(
    (arc) => distribution.hasOwnProperty(arc) && Number(distribution[arc]) > 0
  );
  if (count >= requiredArcs.length) {
    requiredArcs.forEach((arc) => {
      if (plan.includes(arc)) return;
      const counts = {};
      plan.forEach((item) => {
        counts[item] = (counts[item] || 0) + 1;
      });
      const replacement = Object.entries(counts).sort((a, b) => b[1] - a[1])[0];
      if (!replacement || !replacement[0]) return;
      const idx = plan.findIndex((item) => item === replacement[0]);
      if (idx >= 0) plan[idx] = arc;
    });
  }
  return plan;
}

function buildTierPlan({ count, distribution, seed }) {
  const entries = Object.entries(distribution || {}).filter(([, v]) => typeof v === "number" && v > 0);
  if (!entries.length) return [];
  const raw = entries.map(([key, weight]) => ({ key, raw: weight * count }));
  const floors = raw.map((row) => ({ ...row, floor: Math.floor(row.raw), frac: row.raw - Math.floor(row.raw) }));
  let assigned = floors.reduce((acc, row) => acc + row.floor, 0);
  const target = count;
  floors.sort((a, b) => b.frac - a.frac);
  for (let i = 0; assigned < target && i < floors.length; i += 1) {
    floors[i].floor += 1;
    assigned += 1;
  }
  floors.sort((a, b) => a.key.localeCompare(b.key));
  const plan = [];
  floors.forEach((row) => {
    for (let i = 0; i < row.floor; i += 1) plan.push(row.key);
  });
  const rng = createRng(seed);
  return shuffleWithRng(plan, rng);
}

