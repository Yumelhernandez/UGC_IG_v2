function chooseDistinctLine(pool, seenValues, maxChars) {
  if (!Array.isArray(pool) || pool.length === 0) return "";
  const minAllowedDistance = 0.35;
  const seenNormalized = new Set(
    (seenValues || []).map((v) => canonicalHookKey(v || "")).filter(Boolean)
  );
  const candidates = [];
  let best = null;
  let bestScore = -Infinity;
  pool.forEach((line) => {
    const cleaned = sentenceCaseGirlLine(clampMessageText(normalizeMessageText(line), maxChars));
    if (!cleaned) return;
    const normalized = canonicalHookKey(cleaned);
    if (seenNormalized.has(normalized)) return;
    const distance = minDistanceToSet(cleaned, seenValues);
    const maxCos = maxCosineToSet(cleaned, seenValues);
    const score = scoreReply(cleaned) + distance * 1.8 + (1 - maxCos) * 2.2;
    candidates.push({ cleaned, distance, score, maxCos });
  });
  const preferred = candidates.filter((c) => c.distance >= minAllowedDistance);
  const poolToUse = preferred.length > 0 ? preferred : candidates;
  poolToUse.forEach((candidate) => {
    if (candidate.score > bestScore) {
      bestScore = candidate.score;
      best = candidate.cleaned;
    }
  });
  return best || "";
}

function chooseDistinctLineMinWords(pool, seenValues, maxChars, minWords = 3) {
  if (!Array.isArray(pool) || pool.length === 0) return "";
  const minWordPool = pool.filter((line) => countWords(normalizeMessageText(line || "")) >= minWords);
  const preferred = minWordPool.length ? chooseDistinctLine(minWordPool, seenValues, maxChars) : "";
  if (preferred && countWords(preferred) >= minWords) return preferred;
  const fallback = chooseDistinctLine(pool, seenValues, maxChars);
  if (fallback && countWords(fallback) >= minWords) return fallback;
  // Do NOT fall back to minWordPool[0] here — it may be a seen/reused item.
  // Let the caller handle the empty-pool case with a broader pool or last-resort fallback.
  return "";
}

function shuffleWithRng(items, rng) {
  const output = [...items];
  for (let i = output.length - 1; i > 0; i -= 1) {
    const j = Math.floor(rng() * (i + 1));
    [output[i], output[j]] = [output[j], output[i]];
  }
  return output;
}

