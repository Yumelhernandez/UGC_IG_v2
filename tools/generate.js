function hasGirlResistanceBetween(messages, startIndex, endIndex) {
  if (!Array.isArray(messages)) return false;
  if (!Number.isFinite(startIndex) || !Number.isFinite(endIndex) || endIndex - startIndex <= 1) return false;
  for (let i = startIndex + 1; i < endIndex; i += 1) {
    const m = messages[i];
    if (!m || m.from !== "girl") continue;
    if (/\b555\s?\d{3}\s?\d{4}\b/.test(m.text || "")) continue;
    if (isGirlResistanceOrConditionLine(m.text || "")) return true;
  }
  return false;
}

function hasStructuredNumberExchangeFlow(messages) {
  const phoneIdx = findFirstPhoneDropIndex(messages);
  if (phoneIdx < 0) return false;
  const askIdx = findLastStrongAskBeforeIndex(messages, phoneIdx);
  if (askIdx < 0) return false;
  return hasGirlResistanceBetween(messages, askIdx, phoneIdx);
}

