function evaluateScriptQuality({ replyText, messages, maxChars, girlName, format }) {
  const reasons = [];
  const reply = normalizeMessageText(replyText || "");
  if (!reply) reasons.push("empty reply");
  if (reply.length > maxChars) reasons.push("reply too long");
  if (hasAnyPattern(reply, AWKWARD_PHRASE_PATTERNS)) reasons.push("awkward reply");
  if (looksNonEnglish(reply)) reasons.push("non-english reply");

  const seen = new Set();
  messages.forEach((message, index) => {
    if (!message || !message.text) return;
    const cleaned = normalizeMessageText(message.text);
    if (!cleaned) reasons.push(`empty line ${index}`);
    if (cleaned.length > maxChars) reasons.push(`line too long ${index}`);
    if (hasAnyPattern(cleaned, AWKWARD_PHRASE_PATTERNS)) reasons.push(`awkward line ${index}`);
    if (looksNonEnglish(cleaned)) reasons.push(`non-english line ${index}`);
    const signature = cleaned.toLowerCase();
    if (seen.has(signature)) reasons.push(`duplicate line ${index}`);
    seen.add(signature);
    const wordCount = cleaned.split(/\s+/).filter(Boolean).length;
    const girlWordLimit = format === "C" || format === "D" ? 10 : 8;
    if (message.from === "girl") {
      if (wordCount > girlWordLimit) reasons.push(`girl line too long ${index}`);
    }
    if (message.from === "boy") {
      // Exempt forced punchline templates (list_reveal, presumptive_close) from word count
      const isPunchlineTemplate = /i got \d+ things|already told my mom|told my boys/i.test(cleaned);
      if (wordCount > 12 && !isPunchlineTemplate) reasons.push(`boy line too long ${index}`);
    }
    if (message.from === "boy" && girlName && containsName(cleaned, girlName)) {
      reasons.push(`boy line uses girl name ${index}`);
    }
    // Flag AI-sounding mid-lines
    if (message.from === "boy" && /interesting of you|noted.*ill remember|problem is you|you tease the.*then/i.test(cleaned)) {
      reasons.push(`boy line sounds AI ${index}`);
    }
  });

  // PRD V2: endings vary by arc; avoid hard-coding a single close shape in generation quality.

  const softOnlyPrefixes = [
    "pushback opener weak",
    "ask line weak",
    "win line weak",
    "win timing off",
    "reveal timing off"
  ];
  const hardReasons = reasons.filter(
    (reason) => !softOnlyPrefixes.some((prefix) => reason.startsWith(prefix))
  );
  return { pass: hardReasons.length === 0, reasons: hardReasons };
}

function tokenizeForSimilarity(text) {
  return normalizeMessageText(text || "")
    .toLowerCase()
    .replace(/[^a-z0-9\s]/g, " ")
    .split(/\s+/)
    .filter(Boolean);
}

function buildNgramSet(tokens, n = 3) {
  const out = new Set();
  if (!Array.isArray(tokens) || tokens.length === 0) return out;
  if (tokens.length < n) {
    out.add(tokens.join(" "));
    return out;
  }
  for (let i = 0; i <= tokens.length - n; i += 1) {
    out.add(tokens.slice(i, i + n).join(" "));
  }
  return out;
}

function jaccardSimilarity(setA, setB) {
  if (!setA || !setB || setA.size === 0 || setB.size === 0) return 0;
  let intersect = 0;
  for (const item of setA) {
    if (setB.has(item)) intersect += 1;
  }
  const union = setA.size + setB.size - intersect;
  return union > 0 ? intersect / union : 0;
}

function lineSimilarity(a, b) {
  const tokensA = tokenizeForSimilarity(a);
  const tokensB = tokenizeForSimilarity(b);
  if (tokensA.length < 2 || tokensB.length < 2) return 0;
  const gramsA = buildNgramSet(tokensA, 3);
  const gramsB = buildNgramSet(tokensB, 3);
  return jaccardSimilarity(gramsA, gramsB);
}

function extractOpenerPattern(text) {
  const tokens = tokenizeForSimilarity(text);
  if (!tokens.length) return "";
  return tokens.slice(0, Math.min(3, tokens.length)).join(" ");
}

// Semantic concept clusters — openers that are "the same idea" even if worded differently
const OPENER_CONCEPT_MAP = [
  { concept: "why_single", patterns: [/why.*single/i, /why.*boyfriend/i, /why.*girlfriend/i, /why.*taken/i, /don't.*have.*boyfriend/i, /don't.*have.*girl/i] },
  { concept: "complaint_legal", patterns: [/complaint/i, /filing/i, /noise complaint/i, /suing/i, /pressing charges/i, /lawsuit/i, /report.*profile/i] },
  { concept: "quick_question", patterns: [/^quick question/i, /^serious question/i, /^honest question/i, /^real question/i] },
  { concept: "already_told", patterns: [/already told.*mom/i, /already told.*friends/i, /already told.*kids/i, /already told.*future/i] },
  { concept: "emotional_damage", patterns: [/emotional damage/i, /emotional distress/i, /ruined.*life/i, /destroyed.*peace/i] },
  { concept: "screenshot_profile", patterns: [/screenshot.*profile/i, /screenshotted/i, /saved.*profile/i] },
  { concept: "bet_dare", patterns: [/^i bet/i, /^i dare/i, /^bet you/i, /^dare you/i] },
  { concept: "look_like_trouble", patterns: [/look like.*trouble/i, /trouble.*look/i, /manage.*look.*trouble/i] },
];

function classifyOpenerConcept(text) {
  if (!text) return null;
  const normalized = String(text).toLowerCase();
  for (const { concept, patterns } of OPENER_CONCEPT_MAP) {
    for (const re of patterns) {
      if (re.test(normalized)) return concept;
    }
  }
  return null;
}

function extractBoyOpener(messages) {
  const firstBoy = (messages || []).find((m) => m && m.from === "boy" && m.text);
  return firstBoy ? String(firstBoy.text) : "";
}

function classifyOpenerIntensity(replyText) {
  const normalized = normalizeMessageText(replyText || "").toLowerCase();
  if (!normalized) return "neutral";
  if (/(you( are|'re)?\s+mid|taste as good|inside out|thighs|earmuffs|criminal|lawsuit|pressing charges|ruined|dangerous|i need yall both)/.test(normalized)) {
    return "challenge_heavy";
  }
  if (/(bet|dare|complaint|sue|suing|question|how|why|\?)/.test(normalized)) {
    return "provocative";
  }
  return "neutral";
}

function requiredDismissiveRunForOpener(format, formatVariant, replyText) {
  const base = format === "B" && formatVariant === "long" ? 3 : 2;
  const intensity = classifyOpenerIntensity(replyText);
  if (intensity === "challenge_heavy") return base;
  if (intensity === "provocative") return Math.max(1, base - 1);
  return 1;
}

function hasDismissiveRunBeforeReveal(messages, revealIndex, minRun = 2) {
  if (!Array.isArray(messages) || revealIndex <= 0) return false;
  const isDismissive = (text) =>
    VALIDATOR_DISMISSIVE_PATTERNS.some((pattern) => pattern.test(String(text || "").toLowerCase()));
  let run = 0;
  for (let i = 0; i < Math.min(revealIndex, messages.length); i += 1) {
    const msg = messages[i];
    if (!msg || msg.from !== "girl") continue;
    const text = String(msg.text || "");
    if (isDismissive(text) && !/555\s?\d{3}\s?\d{4}/.test(text)) {
      run += 1;
      if (run >= minRun) return true;
    } else {
      run = 0;
    }
  }
  return false;
}

function evaluateArcMechanicsContract({ messages, beats, arcType, format, formatVariant, replyText }) {
  const reasons = [];
  const revealIndex = beats && Number.isFinite(beats.reveal_index) ? beats.reveal_index : -1;
  const lastMessage = Array.isArray(messages) && messages.length ? messages[messages.length - 1] : null;
  const phoneInThread = (messages || []).some(
    (m) => m && m.from === "girl" && /555\s?\d{3}\s?\d{4}/.test(m.text || "")
  );
  const minDismissiveRun = requiredDismissiveRunForOpener(format, formatVariant, replyText);
  if (arcType !== "comedy" && minDismissiveRun > 1 && !hasDismissiveRunBeforeReveal(messages, revealIndex, minDismissiveRun)) {
    reasons.push("missing dismissive run before reveal");
  }

  if (arcType === "number_exchange") {
    const hasAsk = (messages || []).some((m) => m && m.from === "boy" && isStrongAskLine(m.text || ""));
    const hasClose = Boolean(lastMessage && lastMessage.from === "girl" && isGirlCloseMessage(lastMessage.text || ""));
    // Date plan closes and girl-initiated closes are valid alternatives to phone number exchanges
    const hasDatePlanBoy = (messages || []).some((m) => m && m.from === "boy" && /\b(friday|saturday|sunday|thursday|tomorrow|this weekend|tonight)\b/i.test(m.text || "") && /\bat \d|you pick|i'll handle|my treat|dont eat|dress|i got plans|first then|night\b/i.test(m.text || ""));
    const hasGirlInitiated = (messages || []).slice(-3).some((m) => m && m.from === "girl" && /\b(ok fine|ugh fine|fine you win|you earned|just text me|i'll give you|dont blow it|sounds like a plan)\b/i.test(m.text || ""));
    if (!hasAsk && !hasDatePlanBoy) reasons.push("number_exchange missing strong ask");
    if (!hasClose && !phoneInThread && !hasDatePlanBoy && !hasGirlInitiated) reasons.push("number_exchange missing conversion close");
  } else if (arcType === "rejection") {
    const rejectionCue = lastMessage && /\b(no|nah|not|pass|no shot|not today|still no)\b/i.test(lastMessage.text || "");
    if (phoneInThread) reasons.push("rejection arc contains phone number");
    if (!rejectionCue) reasons.push("rejection ending lacks dismissal");
  } else if (arcType === "cliffhanger") {
    const cliffCue =
      lastMessage &&
      /\b(maybe|later|we'?ll see|youll see|not yet|for now|tomorrow|pause|keep wondering)\b/i.test(
        lastMessage.text || ""
      );
    if (phoneInThread) reasons.push("cliffhanger arc contains phone number");
    if (!cliffCue) reasons.push("cliffhanger ending too resolved");
  } else if (arcType === "plot_twist") {
    // Plot twist is a feeling, not a keyword — semantic validation belongs in the LLM prompt,
    // not a regex. Just verify the reveal message exists and has substance.
    const revealMsg = revealIndex >= 0 && messages[revealIndex] ? String(messages[revealIndex].text || "") : "";
    if (!revealMsg || revealMsg.trim().length < 8) {
      reasons.push("plot_twist reveal missing or empty");
    }
  } else if (arcType === "comedy") {
    // Phone is valid for the comedy_number sub-ending — no check needed here
  }

  return { pass: reasons.length === 0, reasons };
}

function evaluateDiversityGate({ messages, recentBanterBoyLines, recentReplies, replyText, config }) {
  const reasons = [];
  const policy = (config && config.diversity_policy) || {};
  const maxOpenerShare =
    typeof policy.max_opener_pattern_share === "number" ? policy.max_opener_pattern_share : 0.18;
  const maxLineSimilarity =
    typeof policy.max_line_similarity === "number"
      ? policy.max_line_similarity
      : Number(config && config.script_quality && config.script_quality.novelty_similarity_jaccard) || 0.84;

  const boyLines = (messages || []).filter((m) => m && m.from === "boy" && m.text).map((m) => String(m.text));
  if (!boyLines.length) return { pass: false, reasons: ["missing boy lines"] };

  for (const line of boyLines) {
    if (/^\s*bold\b/i.test(line)) {
      reasons.push("boy line starts with banned opener stem");
      break;
    }
  }

  const history = Array.isArray(recentBanterBoyLines) ? recentBanterBoyLines : [];
  const opener = extractBoyOpener(messages);
  const openerPattern = extractOpenerPattern(opener);
  if (openerPattern && history.length >= 8) {
    let same = 0;
    for (const line of history) {
      if (extractOpenerPattern(line) === openerPattern) same += 1;
    }
    const share = same / Math.max(1, history.length);
    if (share > maxOpenerShare) {
      reasons.push(`opener pattern overused (${openerPattern})`);
    }
  }

  for (const candidate of boyLines) {
    let maxSeenSimilarity = 0;
    for (const prev of history) {
      const score = lineSimilarity(candidate, prev);
      if (score > maxSeenSimilarity) maxSeenSimilarity = score;
      if (maxSeenSimilarity >= maxLineSimilarity) break;
    }
    if (maxSeenSimilarity >= maxLineSimilarity) {
      reasons.push("boy line too similar to recent history");
      break;
    }
  }

  if (typeof replyText === "string" && replyText.trim()) {
    const openerKey = canonicalHookKey(replyText);
    const replyHistory = Array.isArray(recentReplies) ? recentReplies : [];
    const reused = replyHistory.some((line) => canonicalHookKey(line) === openerKey);
    if (reused) {
      reasons.push("story reply reused within novelty window");
    }
    // CONCEPT-LEVEL duplicate check — catches semantic duplicates like
    // "why are you single" vs "why don't you have a boyfriend"
    const replyConcept = classifyOpenerConcept(replyText);
    if (replyConcept) {
      const conceptCount = replyHistory.filter((line) => classifyOpenerConcept(line) === replyConcept).length;
      // STRUCTURAL FIX 2026-03-11: threshold 1 was too strict — any concept used once was blocked forever.
      // Raised to 5 to allow occasional reuse while still preventing flooding.
      // Banned phrases QA still catches the worst offenders.
      const conceptMaxPerWindow = 25; // high threshold — banned_phrases QA catches the actual bad content
      if (conceptCount >= conceptMaxPerWindow) {
        reasons.push(`opener concept reused: ${replyConcept} (${conceptCount + 1}x in ${replyHistory.length} recent, max ${conceptMaxPerWindow})`);
      }
    }
  }

  return { pass: reasons.length === 0, reasons };
}

function extractDatePrefix(value) {
  const match = String(value || "").match(/^(\d{4}-\d{2}-\d{2})/);
  return match ? match[1] : null;
}

function parseDatePrefix(value) {
  const prefix = extractDatePrefix(value);
  if (!prefix) return null;
  const parsed = new Date(`${prefix}T00:00:00Z`);
  if (Number.isNaN(parsed.getTime())) return null;
  return parsed;
}

function computeWindowCutoffDate(baseDate, windowDays) {
  const safeWindow = Math.max(1, Number(windowDays) || 1);
  const cutoff = new Date(baseDate.getTime());
  cutoff.setUTCDate(cutoff.getUTCDate() - (safeWindow - 1));
  return cutoff;
}

function listScriptDateDirsWithinWindow({ scriptsRootDir, outDir, batchDate, windowDays }) {
  if (!fs.existsSync(scriptsRootDir)) return [];
  const cutoff = computeWindowCutoffDate(batchDate, windowDays);
  try {
    return fs
      .readdirSync(scriptsRootDir)
      .filter((dirName) => {
        if (!/^\d{4}-\d{2}-\d{2}/.test(dirName)) return false;
        const dirPath = path.join(scriptsRootDir, dirName);
        if (dirPath === outDir) return false;
        const dirDate = parseDatePrefix(dirName);
        if (!dirDate) return false;
        return dirDate >= cutoff && dirDate <= batchDate;
      })
      .sort();
  } catch (_) {
    return [];
  }
}

function loadPersistentStoryAssetHistory({ rootDir, cutoffDate, batchDate }) {
  const historyPath = path.join(rootDir, "logs", "story-asset-history.json");
  if (!fs.existsSync(historyPath)) return { usedAssets: new Set(), entries: [] };
  try {
    const raw = JSON.parse(fs.readFileSync(historyPath, "utf8"));
    const entries = Array.isArray(raw) ? raw : [];
    const usedAssets = new Set();
    entries.forEach((entry) => {
      if (!entry || !entry.asset) return;
      const entryDate = parseDatePrefix(entry.batch_date || entry.date || entry.video_id);
      if (!entryDate) return;
      if (entryDate >= cutoffDate && entryDate <= batchDate) {
        usedAssets.add(String(entry.asset));
      }
    });
    return { usedAssets, entries };
  } catch (_) {
    return { usedAssets: new Set(), entries: [] };
  }
}

function savePersistentStoryAssetHistory({ rootDir, existingEntries, newEntries, keepDays = 60 }) {
  const historyPath = path.join(rootDir, "logs", "story-asset-history.json");
  const now = new Date();
  const cutoff = new Date(now.getTime());
  cutoff.setUTCDate(cutoff.getUTCDate() - Math.max(1, Number(keepDays) || 60));
  const merged = [...(Array.isArray(existingEntries) ? existingEntries : []), ...(Array.isArray(newEntries) ? newEntries : [])];
  const filtered = merged.filter((entry) => {
    if (!entry || !entry.asset) return false;
    const entryDate = parseDatePrefix(entry.batch_date || entry.date || entry.video_id);
    if (!entryDate) return false;
    return entryDate >= cutoff;
  });
  ensureDir(path.dirname(historyPath));
  fs.writeFileSync(historyPath, `${JSON.stringify(filtered, null, 2)}\n`, "utf8");
}

function enforceWinLine({ messages, beats, rng, maxChars, spice, usedGirlLines, arcType }) {
  const index = beats.win_index;
  if (!Number.isFinite(index) || index < 0 || index >= messages.length) {
    return { messages, beats };
  }
  if (messages[index].from !== "girl") {
    return { messages, beats };
  }
  // If the ending already has a phone number in recent girl lines, it's already new-style
  if (isNewStyleEnding(messages)) {
    return { messages, beats };
  }

  // Always replace with the new Type B/C ending system
  const ending = buildNewEnding(rng, spice || "medium", maxChars, usedGirlLines, {
    forceType: arcType === "number_exchange" ? "C" : null
  });
  const endingMsgs = ending.endingMessages;

  // Find the boy's ask line (second-to-last) and replace from there
  // For Type B: replace last 2 messages (boy ask + girl win) with 3 new ones
  // For Type C: replace last 2 messages with 5 new ones
  const askIndex = index - 1;
  const beforeEnding = askIndex > 0 ? messages.slice(0, askIndex) : messages.slice(0, Math.max(0, index));
  const updated = [...beforeEnding, ...endingMsgs];

  // Recompute win_index to point to the last message
  const newBeats = { ...beats, win_index: updated.length - 1 };
  return { messages: updated, beats: newBeats };
}

function distributeSegment({ startTime, endTime, count, minGap }) {
  if (count <= 1) return [endTime];
  const minEnd = startTime + minGap * (count - 1);
  const boundedEnd = Math.max(endTime, minEnd);
  const step = (boundedEnd - startTime) / (count - 1);
  return Array.from({ length: count }, (_, i) => startTime + step * i);
}

function applyTypeJitter(times, rng, minGap, baseStart) {
  if (!Array.isArray(times) || !times.length) return times;
  const jittered = times.map((time) => {
    if (!Number.isFinite(time)) return time;
    const magnitude = randBetween(rng, 0.1, 0.3);
    const sign = rng() < 0.5 ? -1 : 1;
    return time + sign * magnitude;
  });
  const output = [];
  for (let i = 0; i < jittered.length; i += 1) {
    const raw = jittered[i];
    if (!Number.isFinite(raw)) {
      output.push(raw);
      continue;
    }
    const minTime = i === 0 ? baseStart : (output[i - 1] ?? baseStart) + minGap;
    output.push(Math.max(raw, minTime));
  }
  return output;
}

function injectFirstResponseVariance({ times, rng, baseStart, format, formatVariant }) {
  if (!Array.isArray(times) || times.length < 1) return times;
  if (format !== "B") return times;
  const isLong = formatVariant === "long";
  const targetResponse = isLong
    ? randBetween(rng, 3.0, 8.0)
    : randBetween(rng, 1.6, 4.2);
  const currentFirst = Number(times[0]);
  if (!Number.isFinite(currentFirst)) return times;
  const desiredFirst = Math.max(baseStart, targetResponse);
  const delta = desiredFirst - currentFirst;
  if (Math.abs(delta) < 0.05) return times;
  return times.map((time) => (Number.isFinite(time) ? time + delta : time));
}

function injectFirstGapVariance({ times, rng, minGap, format, formatVariant }) {
  if (!Array.isArray(times) || times.length < 2) return times;
  if (format !== "B") return times;
  const isLong = formatVariant === "long";
  const targetGap = isLong
    ? randBetween(rng, 2.2, 5.8)
    : randBetween(rng, 1.1, 3.4);
  const first = Number(times[0]);
  const currentSecond = Number(times[1]);
  if (!Number.isFinite(first) || !Number.isFinite(currentSecond)) return times;
  const desiredSecond = Math.max(first + minGap, first + targetGap);
  const delta = desiredSecond - currentSecond;
  if (Math.abs(delta) < 0.05) return times;
  const shifted = times.map((time, idx) => (idx === 0 ? time : time + delta));
  const output = [];
  for (let i = 0; i < shifted.length; i += 1) {
    const raw = shifted[i];
    if (!Number.isFinite(raw)) {
      output.push(i === 0 ? first : (output[i - 1] || first) + minGap);
      continue;
    }
    if (i === 0) {
      output.push(raw);
      continue;
    }
    output.push(Math.max(raw, output[i - 1] + minGap));
  }
  return output;
}

function stretchOrCompressTimingPlan({
  times,
  baseStart,
  minGap,
  minDuration,
  maxDuration
}) {
  if (!Array.isArray(times) || times.length === 0) return times;
  const first = Number.isFinite(times[0]) ? times[0] : baseStart;
  const last = Number.isFinite(times[times.length - 1]) ? times[times.length - 1] : first;
  const currentSpan = Math.max(0.1, last - first);
  const targetSpan = clamp(currentSpan, minDuration, maxDuration);
  if (Math.abs(targetSpan - currentSpan) < 0.25) return times;
  const scale = targetSpan / currentSpan;
  const output = [];
  for (let i = 0; i < times.length; i += 1) {
    const raw = first + (times[i] - first) * scale;
    const minTime = i === 0 ? baseStart : output[i - 1] + minGap;
    output.push(Math.max(raw, minTime));
  }
  return output;
}

function sprinkleGirlReactions({ messages, beats, maxChars, rng }) {
  if (!messages || messages.length === 0) return messages;
  const reactions = ["😭😭", "😳", "👀", "😂"];
  const candidates = messages
    .map((message, index) => ({ message, index }))
    .filter(({ message, index }) => {
      if (!message || message.from !== "girl") return false;
      if (index === beats.pushback_index) return false;
      if (index === beats.win_index) return false;
      return true;
    });
  if (candidates.length === 0) return messages;
  const count = Math.min(candidates.length, rng() < 0.6 ? 1 : 2);
  const picked = new Set();
  const updated = messages.map((message, index) => {
    if (!message || message.from !== "girl") return message;
    if (picked.size >= count) return message;
    const candidate = candidates[Math.floor(rng() * candidates.length)];
    if (!candidate || candidate.index !== index) return message;
    if (picked.has(index)) return message;
    const reaction = reactions[Math.floor(rng() * reactions.length)];
    const text = message.text || "";
    if (text.includes("😭") || text.includes("😳") || text.includes("👀") || text.includes("😂")) {
      return message;
    }
    const appended = `${text} ${reaction}`.trim();
    if (appended.length > maxChars) return message;
    picked.add(index);
    return { ...message, text: appended };
  });
  return updated;
}

