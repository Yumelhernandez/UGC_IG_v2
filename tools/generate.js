async function buildPushbackLine({
  config,
  format,
  replyText,
  storyCaption,
  spice,
  controversyTier,
  maxChars,
  rng,
  recentPushbacks
}) {
  if (format === "D") return "";
  const pushbackConfig = config.pushback || {};
  const banterConfig = config.banter || {};
  const primaryModel = pushbackConfig.model || banterConfig.model || "gpt-5.1";
  const fallbackModels = Array.isArray(pushbackConfig.fallback_models)
    ? pushbackConfig.fallback_models
    : Array.isArray(banterConfig.fallback_models)
    ? banterConfig.fallback_models
    : ["gpt-4o-mini"];
  const runtime = resolveLlmRuntime({
    config,
    sectionKey: "pushback",
    model: primaryModel,
    fallbackModels
  });
  if (!runtime.apiKey) return "";
  const modelsToTry = [runtime.model, ...runtime.fallbackModels];
  const temperature =
    typeof pushbackConfig.temperature === "number"
      ? pushbackConfig.temperature
      : typeof banterConfig.temperature === "number"
      ? Math.min(1.25, banterConfig.temperature + 0.15)
      : 1.1;
  const numOptions = pushbackConfig.num_options || 6;
  const maxOutputTokens = pushbackConfig.max_output_tokens || 120;
  const maxAttempts = pushbackConfig.max_attempts || 4;
  const bannedPhrases = [
    ...(config.banned_phrases || []),
    ...((pushbackConfig && pushbackConfig.banned_phrases) || []),
    ...PUSHBACK_BANNED_PHRASES
  ];
  const avoidPushbacks = Array.isArray(recentPushbacks) ? recentPushbacks.slice(-12) : [];

  let lastError = null;
  for (const model of modelsToTry) {
    let attempt = 0;
    while (attempt < maxAttempts) {
      attempt += 1;
      try {
        const options = await generatePushbackOptions({
          provider: runtime.provider,
          apiKey: runtime.apiKey,
          model,
          temperature,
          maxOutputTokens,
          boyReplyText: replyText,
          storyCaption,
          spiceTier: spice,
          controversyTier,
          bannedPhrases,
          avoidPushbacks,
          numOptions,
          format
        });
        const seen = new Set();
        const filtered = [];
        options.forEach((option) => {
          const cleaned = clampMessageText(normalizeMessageText(option || ""), maxChars);
          if (!cleaned) return;
          if (cleaned.includes("-")) return;
          if (containsBannedText(cleaned, bannedPhrases)) return;
          if (hasAnyPattern(cleaned, AWKWARD_PHRASE_PATTERNS)) return;
          if (looksNonEnglish(cleaned)) return;
          const lower = cleaned.toLowerCase();
          if (seen.has(lower)) return;
          if (avoidPushbacks.some((line) => normalizeMessageText(line).toLowerCase() === lower)) return;
          if (!isStrongPushbackLine(cleaned)) return;
          seen.add(lower);
          filtered.push(cleaned);
        });
        if (filtered.length > 0) {
          const choice = rng ? pick(rng, filtered) : filtered[0];
          return sentenceCaseGirlLine(choice);
        }
      } catch (error) {
        lastError = error;
      }
    }
  }
  if (lastError) {
    console.warn(`Pushback generation failed: ${lastError.message}`);
  }
  return "";
}

function ensurePushbackLine(messages, maxChars, format, replyText, pushbackOverride, forceOverride) {
  if (!messages.length) return { messages, pushbackIndex: 0 };
  const first = messages[0];
  const isFormatD = format === "D";
  const firstValid =
    first &&
    first.from === "girl" &&
    (isFormatD
      ? isFormatDStrongPushbackLine(first.text) && isFormatDReplyTiedToBoy(first.text, replyText)
      : isStrongPushbackLine(first.text));
  if (firstValid && !forceOverride) {
    return { messages, pushbackIndex: 0 };
  }
  const overrideClean = pushbackOverride
    ? sentenceCaseGirlLine(clampMessageText(normalizeMessageText(pushbackOverride), maxChars))
    : "";
  const replacement = isFormatD
    ? buildFormatDPushbackLine(replyText, maxChars)
    : overrideClean || formatReactionLineFromReply(replyText, maxChars) || pickFallbackLine(getPushbackPool(format), maxChars);
  const updated = messages.map((message, i) =>
    i === 0 && message.from === "girl" ? { ...message, text: replacement } : message
  );
  return { messages: updated, pushbackIndex: 0 };
}

function enforceFirstGirlResponseLength(messages, maxChars, maxWords = 5, replyText = "", rng = null) {
  if (!Array.isArray(messages) || messages.length === 0) return messages;
  const firstGirlIndex = messages.findIndex((message) => message && message.from === "girl");
  if (firstGirlIndex < 0) return messages;
  const firstGirl = messages[firstGirlIndex];
  const words = String(firstGirl.text || "").trim().split(/\s+/).filter(Boolean);
  const normalized = normalizeMessageText(firstGirl.text || "");
  const lowSignal =
    /^(no|nah|pass|nice try|prove it|who even|ok|k)$/i.test(normalized);
  if (words.length <= maxWords && words.length >= 1 && !lowSignal) return messages;
  const fallbackPool = [
    "what??",
    "excuse me??",
    "wait what",
    "huh? how?",
    "you serious?",
    "how tho?"
  ];
  const regen = formatReactionLineFromReply(replyText, maxChars, rng) || pickFallbackLine(fallbackPool, maxChars);
  const output = [...messages];
  output[firstGirlIndex] = {
    ...firstGirl,
    text: sentenceCaseGirlLine(regen)
  };
  return output;
}

function computeBeats(messages, pushbackIndex, format) {
  const winIndex = Math.max(0, messages.length - 1);
  let revealIndex = -1;

  if (format === "D") {
    // For Format D (16+ messages), place reveal around the midpoint so the
    // boy banters for a while before dropping his punch line.
    const midpoint = Math.floor(messages.length / 2);
    const searchStart = Math.max(pushbackIndex + 1, midpoint - 1);
    for (let i = searchStart; i < messages.length - 1; i += 1) {
      if (messages[i].from === "boy") {
        revealIndex = i;
        break;
      }
    }
  } else {
    const searchStart =
      format === "B"
        ? Math.min(messages.length - 1, Math.max(pushbackIndex + 3, 3))
        : Math.min(pushbackIndex + 1, messages.length - 1);
    for (let i = searchStart; i < messages.length; i += 1) {
      if (messages[i].from === "boy") {
        revealIndex = i;
        break;
      }
    }
  }

  if (revealIndex < 0) {
    revealIndex = Math.max(0, messages.length - 2);
  }
  if (revealIndex >= winIndex) {
    revealIndex = Math.max(0, winIndex - 1);
  }
  const minShareable = Math.max(1, Math.floor(messages.length * 0.2));
  const maxShareable = Math.max(minShareable, Math.floor(messages.length * 0.35));
  const shareableIndex = Math.max(minShareable, Math.min(maxShareable, revealIndex));
  const revealLine = messages[revealIndex] ? messages[revealIndex].text : "";
  return {
    pushback_index: pushbackIndex,
    reveal_index: revealIndex,
    win_index: winIndex,
    shareable_index: Math.min(winIndex, shareableIndex),
    reveal_line: revealLine
  };
}

function buildBeatPlan({ replyText, messages, beats, arcType }) {
  const firstReaction = messages[beats.pushback_index] ? messages[beats.pushback_index].text : "";
  const escalation = messages[beats.reveal_index] ? messages[beats.reveal_index].text : "";
  const shareable = messages[beats.shareable_index] ? messages[beats.shareable_index].text : escalation;
  const preCloseIndex = Math.max(0, beats.win_index - 1);
  const preClose = messages[preCloseIndex] ? messages[preCloseIndex].text : "";
  const shift = messages[Math.min(beats.reveal_index + 1, beats.win_index)]
    ? messages[Math.min(beats.reveal_index + 1, beats.win_index)].text
    : escalation;
  const close = messages[beats.win_index] ? messages[beats.win_index].text : "";
  return {
    inciting_incident: replyText,
    first_reaction: firstReaction,
    hook: replyText,
    test: firstReaction,
    escalation_turn: escalation,
    escalation,
    shift,
    close,
    shareable_moment: shareable,
    pre_close_tension: preClose,
    required_markers: ["hook", "test", "escalation", "shift", "close"],
    resolution_type: arcType
  };
}

function classifyResponseType({ message, index, beats, arcType, total }) {
  if (!message) return undefined;
  if (index === beats.win_index) {
    if (arcType === "rejection") return "dismissal";
    if (arcType === "cliffhanger") return "test";
    if (arcType === "comedy") return "punchline";
    return "close";
  }
  if (index === beats.pushback_index) return "challenge";
  if (index === beats.reveal_index) return "tease";
  if (index > beats.reveal_index && index >= Math.floor(total * 0.7)) return "condition";
  if (message.from === "girl" && /\b(ok|fine|maybe|sure)\b/i.test(message.text || "")) return "concession";
  if (message.from === "girl") return "dismissal";
  return "test";
}

function classifyFirstReactionType(text) {
  const normalized = normalizeMessageText(text || "").toLowerCase();
  if (!normalized) return "unknown";
  if (/\b(how|why)\b/.test(normalized)) return "curiosity";
  if (/\b(excuse|serious)\b/.test(normalized)) return "challenge";
  if (/\b(huh|what|wait)\b/.test(normalized) || /\?/.test(normalized)) return "confusion";
  return "reaction";
}

function enforceArcEndingContracts({ messages, beats, arcType, rng, maxChars, usedGirlLines, format }) {
  if (!Array.isArray(messages) || messages.length < 3) return messages;
  const updated = [...messages];
  const lastIdx = updated.length - 1;
  // rejectionEndings removed — rejection arc disabled in config
  const cliffhangerEndings = [
    "you'll see",
    "not answering that yet",
    "not giving you that yet",
    "you can wonder for now",
    "thats all youre getting tonight",
    "ill decide when i feel like it",
    "still not telling you",
    "we'll see",
    "you'll find out",
    "not yet",
    "depends on you",
    "ask me again later",
    "i haven't decided yet",
    "maybe. maybe not",
    "that's not an answer you're getting today",
    "use your imagination",
    "check back later"
  ];
  // twistReveals + plotTwistEndings removed — plot_twist arc disabled in config
  const phonePattern = /\b555\s?\d{3}\s?\d{4}\b/g;
  const stripPhone = (text) => {
    const cleaned = String(text || "")
      .replace(phonePattern, "")
      .replace(/^(text|call|hit)\s+me\s*[.,]?\s*/i, "") // remove orphaned "text me" prefix after phone stripped
      .replace(/^[\s.,!?:;-]+/, "") // trim leftover leading punctuation
      .replace(/\s+/g, " ")
      .trim();
    return cleaned;
  };
  // Words that signal the LLM generated a generic filler close — used for comedy check.
  const FILLER_CLOSE_WORDS = new Set([
    "fine", "ok", "okay", "alright", "sure", "yeah",
    "ok fine", "alright fine", "ugh fine", "fine ok",
    "not giving that out", "not giving that away yet"
  ]);
  // For rejection and cliffhanger: always remove any preceding girl message —
  // the pool line IS the close, nothing the LLM wrote before it adds value.
  const collapseAnyGirlMessage = (arr) => {
    const last = arr.length - 1;
    const prev = last - 1;
    if (prev >= 0 && arr[prev] && arr[prev].from === "girl") {
      arr.splice(prev, 1);
    }
  };
  // For comedy sub-endings: only remove filler words, keep real LLM reactions.
  const collapseGirlFiller = (arr) => {
    const last = arr.length - 1;
    const prev = last - 1;
    if (prev >= 0 && arr[prev] && arr[prev].from === "girl") {
      const t = (arr[prev].text || "").toLowerCase().replace(/[^a-z\s]/g, "").trim();
      if (FILLER_CLOSE_WORDS.has(t)) {
        arr.splice(prev, 1);
      }
    }
  };
  const stripPhoneOrFallback = (text, fallback) => {
    const original = String(text || "");
    const hadPhone = phonePattern.test(original);
    phonePattern.lastIndex = 0; // reset regex state after test
    const cleaned = stripPhone(text);
    // If we stripped a phone and the remainder is too short to be meaningful, use fallback
    if (!cleaned || (hadPhone && cleaned.split(/\s+/).filter(Boolean).length <= 2)) return fallback;
    return cleaned;
  };
  const pickUniqueEnding = (pool, fallback) => {
    const normalizedUsed = usedGirlLines || new Set();
    const available = pool.filter((line) => !normalizedUsed.has(String(line).toLowerCase()));
    const selected = pickFallbackLine(available.length ? available : pool, maxChars);
    if (usedGirlLines) usedGirlLines.add(String(selected).toLowerCase());
    return selected || fallback;
  };

  if (arcType === "number_exchange") {
    // NOVELTY FIX: Diversified close sequence — 3 styles instead of always "number ask → phone → tease"
    const DATE_PLAN_CLOSES = [
      "friday at 8. you pick the spot",
      "saturday night. i'll handle the rest",
      "tomorrow. coffee first then we'll see",
      "this weekend. dress comfortable i got plans",
      "thursday at 7. dont eat beforehand",
      "sunday brunch. my treat this time"
    ];
    const GIRL_INITIATED_CLOSES = [
      "ok fine just text me already",
      "ugh here. dont blow it",
      "you earned it honestly",
      "fine you win. text me",
      "alright i'll give you a chance"
    ];

    // Check if a date plan or girl-initiated close already exists (from a prior enforcement pass)
    const _existingDatePlan = updated.some((m) => m && m.from === "boy" && /\b(friday|saturday|sunday|thursday|tomorrow|this weekend|tonight)\b/i.test(m.text || "") && /\bat \d|you pick|i'll handle|my treat|dont eat|dress|i got plans/i.test(m.text || ""));
    const _existingGirlInit = updated.slice(-3).some((m) => m && m.from === "girl" && /\b(ok fine just text|fine you win|you earned it|ugh here)\b/i.test(m.text || ""));

    let closeStyle;
    if (_existingDatePlan) {
      closeStyle = "date_plan";  // preserve prior enforcement's choice
    } else if (_existingGirlInit) {
      closeStyle = "girl_initiated";
    } else {
      const closeStyleRoll = rng();
      closeStyle = closeStyleRoll < 0.50 ? "number_ask"
        : closeStyleRoll < 0.75 ? "date_plan"
        : "girl_initiated";
    }

    let phoneIdx = findFirstPhoneDropIndex(updated);

    if (closeStyle === "date_plan") {
      // Boy proposes plan → girl agrees with personality
      if (phoneIdx >= 0) {
        // LLM already has a phone — strip it and replace with date plan
        const fallback = updated[phoneIdx].from === "girl" ? "sounds like a plan" : "say less";
        updated[phoneIdx] = { ...updated[phoneIdx], text: stripPhoneOrFallback(updated[phoneIdx].text, fallback) };
      }
      const closeBoyIdx = Math.max(0, lastIdx - 1);
      updated[closeBoyIdx] = { ...updated[closeBoyIdx], from: "boy", text: pick(rng, DATE_PLAN_CLOSES) };
      updated[lastIdx] = { ...updated[lastIdx], from: "girl", text: pickUniqueEnding(bankPool("girl_close", FALLBACK_CLOSE_LINES), "dont be late 😭") };
    } else if (closeStyle === "girl_initiated") {
      // Girl drops number without boy asking — she's won over
      if (phoneIdx < 0) {
        phoneIdx = Math.max(1, lastIdx - 1);
        updated[phoneIdx] = { ...updated[phoneIdx], from: "girl",
          text: `text me 555 ${String(Math.floor(rng() * 900) + 100)} ${String(Math.floor(rng() * 9000) + 1000)}`
        };
      }
      // Replace boy's last line before phone with something that EARNS the number (not "what's your number")
      const preBoyIdx = Math.max(0, phoneIdx - 1);
      if (updated[preBoyIdx] && updated[preBoyIdx].from === "boy") {
        // Keep LLM's line if it's not a generic ask — it's more novel
        if (/number|digits|contact/i.test(updated[preBoyIdx].text)) {
          const EARNED_CLOSES = ["i mean it tho", "just say the word", "your call", "so what do you think", "im serious btw"];
          updated[preBoyIdx] = { ...updated[preBoyIdx], text: pick(rng, EARNED_CLOSES) };
        }
      }
      updated[lastIdx] = { ...updated[lastIdx], from: "girl", text: pick(rng, GIRL_INITIATED_CLOSES) };
    } else {
      // Classic: boy asks → girl gives number → tease
      if (phoneIdx < 0) {
        phoneIdx = Math.max(1, lastIdx - 1);
        updated[phoneIdx] = { ...updated[phoneIdx], from: "girl",
          text: `text me 555 ${String(Math.floor(rng() * 900) + 100)} ${String(Math.floor(rng() * 9000) + 1000)}`
        };
        updated[lastIdx] = { ...updated[lastIdx], from: "girl",
          text: pickUniqueEnding(bankPool("girl_close", FALLBACK_CLOSE_LINES), "text me later")
        };
      }
      let askIdx = findLastStrongAskBeforeIndex(updated, phoneIdx);
      if (askIdx < 0) {
        const forcedAskIdx = Math.max(0, phoneIdx - 1);
        updated[forcedAskIdx] = { ...updated[forcedAskIdx], from: "boy", text: pick(rng, BOY_NUMBER_ASK_LINES) };
        askIdx = forcedAskIdx;
      }
      if (!hasGirlResistanceBetween(updated, askIdx, phoneIdx)) {
        if (phoneIdx - askIdx <= 1 && phoneIdx >= 2) {
          const shiftedAskIdx = phoneIdx - 2;
          updated[shiftedAskIdx] = { ...updated[shiftedAskIdx], from: "boy", text: pick(rng, BOY_NUMBER_ASK_LINES) };
          askIdx = shiftedAskIdx;
        }
        const resistanceIdx = Math.min(phoneIdx - 1, askIdx + 1);
        if (resistanceIdx > askIdx) {
          updated[resistanceIdx] = { ...updated[resistanceIdx], from: "girl",
            text: pickUniqueFromPool(GIRL_RESIST_LINES, maxChars, usedGirlLines) || "why should i"
          };
        }
      }
    }
    // Prevent triple-girl at end
    const nFinal = updated.length;
    if (nFinal >= 3 && updated[nFinal-1].from === "girl" && updated[nFinal-2].from === "girl" && updated[nFinal-3].from === "girl") {
      updated.splice(nFinal - 3, 1);
    }
    return updated;
  }

  // NOTE: rejection and plot_twist arc branches removed (2026-03-08 simplification).
  // These arcs are disabled in config.arc_distribution. If re-enabled, add handlers here.

  if (arcType === "cliffhanger") {
    updated[lastIdx] = {
      ...updated[lastIdx],
      from: "girl",
      text: pickUniqueEnding(cliffhangerEndings, "you'll see")
    };
    for (let i = 0; i < updated.length; i += 1) {
      const fallback = updated[i].from === "girl" ? "not giving that away yet" : "say less";
      updated[i] = { ...updated[i], text: stripPhoneOrFallback(updated[i].text, fallback) };
    }
    collapseAnyGirlMessage(updated);
    return updated;
  }
  // NOTE: comedy arc branch removed (2026-03-08 simplification).
  // Comedy is disabled in config.arc_distribution. If re-enabled, add handler here.
  return updated;
}

