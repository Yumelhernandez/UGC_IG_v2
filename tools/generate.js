function normalizeTimingWindow(value, fallback) {
  if (Array.isArray(value) && value.length >= 2) {
    return { min: Number(value[0]), max: Number(value[1]) };
  }
  if (value && typeof value === "object") {
    const min = Number(value.min);
    const max = Number(value.max);
    if (Number.isFinite(min) || Number.isFinite(max)) {
      return { min, max };
    }
  }
  return fallback;
}

function getTimingWindows(config, format) {
  const windows = (config && config.timing_windows) || {};
  const key = format === "A" ? "B" : format || "B";
  const fallback = DEFAULT_TIMING_WINDOWS[key] || DEFAULT_TIMING_WINDOWS.B;
  const entry = windows[key] || windows[format] || windows.B || fallback;
  return {
    reveal: normalizeTimingWindow(entry && entry.reveal, fallback.reveal),
    win: normalizeTimingWindow(entry && entry.win, fallback.win)
  };
}

function clampTiming(value, window, floor) {
  let output = value;
  if (window && Number.isFinite(window.min)) output = Math.max(output, window.min);
  if (window && Number.isFinite(window.max)) output = Math.min(output, window.max);
  if (Number.isFinite(floor) && output < floor) output = floor;
  return output;
}

function applyTimingBuffer(window, buffer) {
  if (!window || !Number.isFinite(buffer)) return window;
  if (!Number.isFinite(window.max)) return window;
  const adjustedMax = window.max - buffer;
  if (Number.isFinite(window.min) && adjustedMax < window.min) return window;
  return { ...window, max: adjustedMax };
}

