// Strip emojis from hook headlines — competitors never use emojis in hook text
const EMOJI_RE = /[\u{1F600}-\u{1F64F}\u{1F300}-\u{1F5FF}\u{1F680}-\u{1F6FF}\u{1F1E0}-\u{1F1FF}\u{2600}-\u{26FF}\u{2700}-\u{27BF}\u{FE00}-\u{FE0F}\u{1F900}-\u{1F9FF}\u{1FA00}-\u{1FA6F}\u{1FA70}-\u{1FAFF}\u{200D}\u{20E3}\u{E0020}-\u{E007F}]/gu;
function stripEmojis(text) {
  return (text || "").replace(EMOJI_RE, "").replace(/\s{2,}/g, " ").trim();
}

function hasViralUiArtifact(text) {
  return VIRAL_UI_ARTIFACT_RE.test(String(text || ""));
}

function isUsableViralConversationExample(example) {
  if (!example || !Array.isArray(example.conversation) || example.conversation.length < 3) return false;
  const opener = String(example.opener || "").trim().toLowerCase();
  if (!opener || opener === "text") return false;
  if (hasViralUiArtifact(opener)) return false;
  for (const msg of example.conversation) {
    if (!msg || (msg.from !== "boy" && msg.from !== "girl")) return false;
    const text = String(msg.text || "").trim();
    if (!text) return false;
    if (hasViralUiArtifact(text)) return false;
  }
  return true;
}

