function normalizedEditDistance(a, b) {
  const x = normalizeMessageText(a || "").toLowerCase();
  const y = normalizeMessageText(b || "").toLowerCase();
  const maxLen = Math.max(1, x.length, y.length);
  let prev = new Array(y.length + 1).fill(0).map((_, i) => i);
  for (let i = 1; i <= x.length; i += 1) {
    const curr = [i];
    for (let j = 1; j <= y.length; j += 1) {
      const cost = x[i - 1] === y[j - 1] ? 0 : 1;
      curr[j] = Math.min(curr[j - 1] + 1, prev[j] + 1, prev[j - 1] + cost);
    }
    prev = curr;
  }
  return prev[y.length] / maxLen;
}

function minDistanceToSet(candidate, values) {
  if (!values || values.length === 0) return 1;
  let min = 1;
  values.forEach((value) => {
    const dist = normalizedEditDistance(candidate, value);
    if (dist < min) min = dist;
  });
  return min;
}

function tokenSet(value) {
  return new Set(
    normalizeMessageText(value || "")
      .toLowerCase()
      .replace(/[^a-z0-9\s]/g, " ")
      .split(/\s+/)
      .filter(Boolean)
  );
}

function tokenCosineSimilarity(a, b) {
  const ta = tokenSet(a);
  const tb = tokenSet(b);
  if (!ta.size || !tb.size) return 0;
  let dot = 0;
  ta.forEach((token) => {
    if (tb.has(token)) dot += 1;
  });
  const denom = Math.sqrt(ta.size) * Math.sqrt(tb.size);
  return denom > 0 ? dot / denom : 0;
}

function maxCosineToSet(candidate, values) {
  if (!values || values.length === 0) return 0;
  let max = 0;
  values.forEach((value) => {
    const c = tokenCosineSimilarity(candidate, value);
    if (c > max) max = c;
  });
  return max;
}

