function enforceResistanceBeat({ messages, beats, format, formatVariant, maxChars, rng, usedGirlLines, replyText }) {
  if (!Array.isArray(messages) || !messages.length) return { messages, beats };
  const requiredRun = requiredDismissiveRunForOpener(format, formatVariant, replyText);
  const dismissivePool = [
    "nah",
    "not happening",
    "pass",
    "nice try",
    "no",
    "prove it",
    "why tho",
    "huh",
    "wdym",
    "cap",
    "who even"
  ];
  const isDismissive = (text) =>
    VALIDATOR_DISMISSIVE_PATTERNS.some((pattern) => pattern.test(String(text || "").toLowerCase()));
  const findMaxRun = (arr, revealIdx) => {
    const beforeShift = revealIdx > 0 ? arr.slice(0, revealIdx) : arr;
    let current = 0;
    let maxRun = 0;
    beforeShift.forEach((m) => {
      if (m && m.from === "girl" && isDismissive(m.text)) {
        current += 1;
        if (current > maxRun) maxRun = current;
      } else if (m && m.from === "girl") {
        current = 0;
      }
    });
    return maxRun;
  };
  let revealIndex = Number.isFinite(beats && beats.reveal_index) ? beats.reveal_index : messages.length - 1;
  const output = [...messages];
  const girlIndicesBeforeReveal = [];
  for (let i = 0; i < Math.max(0, revealIndex); i += 1) {
    if (output[i] && output[i].from === "girl") girlIndicesBeforeReveal.push(i);
  }
  if (girlIndicesBeforeReveal.length < requiredRun) {
    const girlIndicesAll = [];
    for (let i = 0; i < messages.length; i += 1) {
      if (messages[i] && messages[i].from === "girl") girlIndicesAll.push(i);
    }
    const anchorGirl = girlIndicesAll[Math.min(requiredRun - 1, girlIndicesAll.length - 1)];
    if (Number.isFinite(anchorGirl)) {
      for (let i = anchorGirl + 1; i < messages.length; i += 1) {
        if (messages[i] && messages[i].from === "boy") {
          revealIndex = i;
          break;
        }
      }
    }
  }
  const nextBeats = {
    ...beats,
    reveal_index: Math.max(0, Math.min(messages.length - 2, revealIndex))
  };
  let maxRun = findMaxRun(output, nextBeats.reveal_index);
  if (maxRun >= requiredRun || requiredRun <= 1) return { messages: output, beats: nextBeats };

  const girlBeforeShift = [];
  for (let i = 0; i < Math.max(0, nextBeats.reveal_index); i += 1) {
    if (output[i] && output[i].from === "girl") girlBeforeShift.push(i);
  }

  // Deterministic minimal repair: only rewrite as many pre-shift girl turns as needed.
  for (let i = 0; i < girlBeforeShift.length && maxRun < requiredRun; i += 1) {
    const idx = girlBeforeShift[i];
    if (output[idx] && isDismissive(output[idx].text)) continue;
    output[idx] = {
      ...output[idx],
      text: clampMessageText(pickUniqueLineWithRng(dismissivePool, usedGirlLines || null, maxChars, rng), maxChars)
    };
    maxRun = findMaxRun(output, nextBeats.reveal_index);
  }
  return { messages: output, beats: nextBeats };
}

