async function enforceRevealLine({
  messages,
  beats,
  maxChars,
  usedRevealLines,
  usedGirlLines,
  format,
  config,
  replyText,
  storyCaption,
  spice,
  controversyTier,
  rng,
  recentRevealLines
}) {
  const index = beats.reveal_index;
  if (!Number.isFinite(index) || index < 0 || index >= messages.length) {
    return { messages, beats };
  }
  if (messages[index].from !== "boy") {
    return { messages, beats };
  }
  const pushbackIndex = beats && Number.isFinite(beats.pushback_index) ? beats.pushback_index : 0;
  const pushbackText = messages[pushbackIndex] ? messages[pushbackIndex].text : "";
  const revealOverride = await buildRevealLine({
    config,
    format,
    replyText,
    storyCaption,
    pushbackText,
    spice,
    controversyTier,
    maxChars,
    rng,
    recentRevealLines
  });
  const basePool = getRevealPool(format);
  const revealPool = usedRevealLines
    ? basePool.filter((line) => !usedRevealLines.has(line))
    : basePool;
  const revealLine = revealOverride || pickFallbackLine(revealPool.length ? revealPool : basePool, maxChars);
  const updated = messages.map((message, i) => {
    if (i === index) {
      return { ...message, text: revealLine };
    }
    if (i === index + 1 && message.from === "girl") {
      const baseReactionPool =
        format === "D" ? FORMAT_D_REVEAL_REACTION_LINES : REVEAL_REACTION_LINES;
      const reactionPool = usedGirlLines
        ? baseReactionPool.filter((line) => !usedGirlLines.has(line.toLowerCase()))
        : baseReactionPool;
      const reactionLine = pickFallbackLine(
        reactionPool.length ? reactionPool : baseReactionPool,
        maxChars
      );
      return { ...message, text: reactionLine };
    }
    return message;
  });
  if (usedRevealLines) usedRevealLines.add(revealLine);
  if (usedGirlLines && updated[index + 1] && updated[index + 1].from === "girl") {
    usedGirlLines.add(updated[index + 1].text.toLowerCase());
  }
  return { messages: updated, beats: { ...beats, reveal_line: revealLine } };
}

